package extra6;

public class Venta {

    private int ventas;
    private int unidades;

    public Venta(int ventas, int unidades) {
        this.ventas = ventas;
        this.unidades = unidades;
    }

    public int getVentas() {
        return ventas;
    }

    public int getUnidades() {
        return unidades;
    }

}